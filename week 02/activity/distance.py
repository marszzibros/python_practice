#   constant SPEED = 70 (miles)
SPEED = 70

#   calculate the distance that the car can move in 6, 10, 15 hours
distance1 = SPEED * 6
distance2 = SPEED * 10
distance3 = SPEED * 15

#   print the calculated data
print('The distance the car will travel in 6 hours: ', distance1, 'miles', sep='', end='\n')
print('The distance the car will travel in 10 hours: ', distance2, 'miles', sep='', end='\n')
print('The distance the car will travel in 15 hours: ', distance3, 'miles', sep='')